void i2c_write_pcf(uint8_t slave_addr, uint8_t data_write)
{
  HAL_I2C_Master_Transmit(&hi2c1, slave_addr, &data_write, 1, 100);
}

uint8_t i2c_read_pcf(uint8_t slave_addr)
{
  uint8_t rdata;
  rdata = HAL_I2C_Slave_Receive(&hi2c1, &rdata, sizeof(rdata), 100);
  return rdata;
}

